package cn.ac.big.gsa.common.pojo;

public class Accession {
	private String accessionType;
    private String accessionValue;
    
    public String getAccessionType() {
        return accessionType;
    }

    public void setAccessionType(String accessionType) {
        this.accessionType = accessionType;
    }

    public String getAccessionValue() {
        return accessionValue;
    }

    public void setAccessionValue(String accessionValue) {
        this.accessionValue = accessionValue;
    }
}
